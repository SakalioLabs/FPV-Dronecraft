def add_icas_hover_gust(packet: list[dict[str, str]], rows: list[dict[str, str]]) -> None:
    metrics = [
        ("rpm", "rpm"),
        ("gust_speed_m_s", "m/s"),
        ("gust_direction_deg", "deg"),
        ("gust_direction_label", "label"),
        ("ct_no_wind", "CT"),
        ("ct_gust", "CT"),
        ("ct_ratio_gust_over_no_wind", "ratio"),
        ("ct_change_percent", "%"),
    ]
    for row in rows:
        if row.get("row_type") != "reference_icas_hover_gust_ct":
            continue
        for metric, unit in metrics:
            add_metric(
                packet,
                row_type="wind_gust_packet_icas_hover_ct",
                name=row["name"],
                metric=metric,
                value=row.get(metric, ""),
                unit=unit,
                source_file=WIND_REFERENCE,
                source_url=row.get("source", ICAS_GUST_PDF_URL),
                evidence_role="direct_rotor_hover_gust_ct_change",
                note=row.get("note", ""),
            )


def require_one(rows: list[dict[str, str]], predicate) -> dict[str, str]:
    found = [row for row in rows if predicate(row)]
    if len(found) != 1:
        raise LookupError(f"expected one row, found {len(found)}")
    return found[0]


def add_crosscheck(packet: list[dict[str, str]], rows: list[dict[str, str]]) -> None:
    scans = [row for row in rows if row.get("row_type") == "current_wind_gust_scan"]
    icas = [row for row in rows if row.get("row_type") == "reference_icas_hover_gust_ct"]
    representative = require_one(
        scans,
        lambda row: abs(to_float(row, "wind_speed_m_s") - 10.0) <= 1.0e-9
        and abs(to_float(row, "dirty_air") - 1.5) <= 1.0e-9,
    )

    def scan_values(key: str) -> list[float]:
        return clean(to_float(row, key) for row in scans)

    def icas_change(rpm: float, direction: str) -> float:
        row = require_one(
            icas,
            lambda item: abs(to_float(item, "rpm") - rpm) <= 1.0e-9
            and item.get("gust_direction_label") == direction,
        )
        return to_float(row, "ct_change_percent")

    metrics = [
        ("current_wind_scan_count", len(scans), "count"),
        ("icas_hover_gust_row_count", len(icas), "count"),
        ("representative_wind_speed_m_s", to_float(representative, "wind_speed_m_s"), "m/s"),
        ("representative_dirty_air", to_float(representative, "dirty_air"), "input"),
        ("representative_current_gust_rms_x_m_s", to_float(representative, "current_gust_rms_x_m_s"), "m/s"),
        ("representative_current_gust_rms_y_m_s", to_float(representative, "current_gust_rms_y_m_s"), "m/s"),
        ("representative_current_x_rms_over_dryden_u", to_float(representative, "current_x_rms_over_dryden_u"), "ratio"),
        ("representative_current_y_rms_over_dryden_w", to_float(representative, "current_y_rms_over_dryden_w"), "ratio"),
        ("representative_gust_corner_hz", to_float(representative, "current_gust_corner_hz"), "Hz"),
        ("representative_dryden_u_pole_hz", to_float(representative, "dryden_u_pole_hz"), "Hz"),
        ("representative_dryden_w_pole_hz", to_float(representative, "dryden_vw_pole_hz"), "Hz"),
        ("representative_shape_over_dryden_u_at_1hz", to_float(representative, "current_shape_over_dryden_u_at_1hz"), "ratio"),
        ("representative_shape_over_dryden_w_at_1hz", to_float(representative, "current_shape_over_dryden_w_at_1hz"), "ratio"),
        ("current_x_rms_over_dryden_u_min", min(scan_values("current_x_rms_over_dryden_u")), "ratio"),
        ("current_x_rms_over_dryden_u_max", max(scan_values("current_x_rms_over_dryden_u")), "ratio"),
        ("current_y_rms_over_dryden_w_min", min(scan_values("current_y_rms_over_dryden_w")), "ratio"),
        ("current_y_rms_over_dryden_w_max", max(scan_values("current_y_rms_over_dryden_w")), "ratio"),
        ("current_corner_over_dryden_u_pole_min", min(scan_values("current_corner_over_dryden_u_pole")), "ratio"),
        ("current_corner_over_dryden_u_pole_max", max(scan_values("current_corner_over_dryden_u_pole")), "ratio"),
        ("current_shape_over_dryden_u_at_1hz_max", max(scan_values("current_shape_over_dryden_u_at_1hz")), "ratio"),
        ("current_shape_over_dryden_w_at_1hz_max", max(scan_values("current_shape_over_dryden_w_at_1hz")), "ratio"),
        ("icas_4319rpm_downdraft_ct_change_percent", icas_change(4319.0, "-90deg"), "%"),
        ("icas_4319rpm_updraft_ct_change_percent", icas_change(4319.0, "90deg"), "%"),
        ("icas_6528rpm_downdraft_ct_change_percent", icas_change(6528.0, "-90deg"), "%"),
        ("icas_6528rpm_updraft_ct_change_percent", icas_change(6528.0, "90deg"), "%"),
        ("icas_ct_change_percent_min", min(clean(to_float(row, "ct_change_percent") for row in icas)), "%"),
        ("icas_ct_change_percent_max", max(clean(to_float(row, "ct_change_percent") for row in icas)), "%"),
    ]
    for metric, value, unit in metrics:
        add_metric(
            packet,
            row_type="wind_gust_packet_crosscheck",
            name="summary",
            metric=metric,
            value=value,
            unit=unit,
            source_file=WIND_REFERENCE,
            source_url=f"{PYFLY_DRYDEN_URL}; {UAV_WIND_SURVEY_URL}; {ICAS_GUST_PDF_URL}; {DRONE_PHYSICS_SOURCE}",
            evidence_role="handoff_summary",
            note="Use these rows to separate gust-field RMS matching from Dryden spectral shape and direct rotor thrust perturbation.",
        )


def add_method(packet: list[dict[str, str]]) -> None:
    add_metric(
        packet,
        row_type="wind_gust_packet_method",
        name="method",
        metric="scope_and_caveat",
        value=(
            "Use this packet as a wind-field and gust-response handoff. The Dryden rows check low-altitude "
            "turbulence intensity and filter shape; the ICAS rows show direct rotor CT changes under gust direction. "
            "Do not collapse deterministic dirty-air burble, atmospheric Dryden turbulence, and rotor inflow CT "
            "perturbation into one coefficient."
        ),
        unit="text",
        source_file=WIND_REFERENCE,
        source_url=f"{PYFLY_DRYDEN_URL}; {UAV_WIND_SURVEY_URL}; {ICAS_GUST_PDF_URL}",
        evidence_role="method",
    )


def build_packet() -> list[dict[str, str]]:
    source_rows = read_rows(WIND_REFERENCE)
    packet: list[dict[str, str]] = []
    add_source_inventory(packet)
    add_current_intensity_scan(packet, source_rows)
    add_spectral_shape_scan(packet, source_rows)
    add_icas_hover_gust(packet, source_rows)
    add_crosscheck(packet, source_rows)
    add_method(packet)
    return packet


def sync_summary(packet_rows: Iterable[dict[str, str]]) -> int:
    existing = read_rows(SUMMARY)
    kept = [row for row in existing if not row.get("category", "").startswith("wind_gust_packet_")]
    added: list[dict[str, str]] = []
    for row in packet_rows:
        added.append(
            {
                "category": row["row_type"],
                "name": row["name"],
                "metric": row["metric"],
                "value": row["value"],
                "unit": row["unit"],
                "source": row.get("source_url") or row.get("source_file", ""),
            }
        )
    write_csv(SUMMARY, kept + added)
    return len(added)


def main() -> None:
    packet_rows = build_packet()
    write_csv(OUTPUT, packet_rows)
    synced = sync_summary(packet_rows)
    print(f"Wrote {repo_path(OUTPUT)} with {len(packet_rows)} rows")
    print(f"Synced {synced} rows into {repo_path(SUMMARY)}")


if __name__ == "__main__":
    main()

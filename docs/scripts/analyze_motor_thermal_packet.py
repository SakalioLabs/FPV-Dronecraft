"""Build a motor/ESC thermal calibration packet.

Outputs:
  docs/data/motor_thermal_packet.csv
  docs/data/fpv_model_validation_summary.csv rows with category motor_thermal_packet_*

The source rows are generated by the main model-source analysis from public
U8/Kv100 dyno maps, Mini Quad Test Bench FPV motor metadata, copper winding
temperature references, and current project thermal/electrical mirrors. This
packet reshapes the wide thermal table into a narrow handoff table for tuning
motor/ESC thermal rise, cooling, and hot-winding resistance behavior.
"""

from __future__ import annotations

import csv
import math
from pathlib import Path
from typing import Iterable


ROOT = Path(__file__).resolve().parents[2]
DATA = ROOT / "docs" / "data"

OUTPUT = DATA / "motor_thermal_packet.csv"
SUMMARY = DATA / "fpv_model_validation_summary.csv"

MOTOR_THERMAL = DATA / "motor_esc_thermal_reference.csv"
ESC_ELECTRICAL = DATA / "esc_electrical_dynamics_reference.csv"

DRONE_CONFIG_SOURCE = "drone-sim-core/src/main/java/com/tenicana/dronecraft/sim/DroneConfig.java"
U8_SOURCE = "https://github.com/thhsieh/U8-Kv100-Dyno-Data"
MQTB_SOURCE = "https://www.miniquadtestbench.com/assets/components/motordata/motorinfo.php?uid=259"
COPPER_SOURCE = "https://www.copper.org/publications/newsletters/innovations/2001/08/intro_fac.html"
MOTOR_INSULATION_SOURCE = "https://www.theaemt.com/resource/insulation-classes-for-electric-motors.html"
INFINEON_MOSFET_SOURCE = "https://www.infineon.com/part/IRL40SC228"

REFERENCE_AMBIENT_C = 25.0


def repo_path(path: Path) -> str:
    return path.resolve().relative_to(ROOT).as_posix()


def read_rows(path: Path) -> list[dict[str, str]]:
    with path.open(newline="", encoding="utf-8") as handle:
        return list(csv.DictReader(handle))


def write_csv(path: Path, rows: Iterable[dict[str, object]]) -> None:
    rows = list(rows)
    path.parent.mkdir(parents=True, exist_ok=True)
    fieldnames: list[str] = []
    for row in rows:
        for key in row:
            if key not in fieldnames:
                fieldnames.append(key)
    with path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(handle, fieldnames=fieldnames)
        writer.writeheader()
        for row in rows:
            writer.writerow(row)


def value_text(value: object) -> str:
    if value is None:
        return ""
    if isinstance(value, str):
        return value
    if isinstance(value, bool):
        return "1" if value else "0"
    if isinstance(value, int):
        return str(value)
    if isinstance(value, float):
        if not math.isfinite(value):
            return ""
        return f"{value:.12g}"
    return str(value)


def to_float(row: dict[str, str], key: str, default: float = math.nan) -> float:
    raw = row.get(key, "")
    if raw == "":
        return default
    try:
        return float(raw)
    except ValueError:
        return default


def require_one(rows: list[dict[str, str]], predicate) -> dict[str, str]:
    found = [row for row in rows if predicate(row)]
    if len(found) != 1:
        raise LookupError(f"expected one row, found {len(found)}")
    return found[0]


def add_metric(
    rows: list[dict[str, str]],
    *,
    row_type: str,
    name: str,
    metric: str,
    value: object,
    unit: str,
    source_file: Path,
    source_url: str = "",
    evidence_role: str = "",
    note: str = "",
) -> None:
    rows.append(
        {
            "row_type": row_type,
            "name": name,
            "metric": metric,
            "value": value_text(value),
            "unit": unit,
            "source_file": repo_path(source_file),
            "source_url": source_url,
            "evidence_role": evidence_role,
            "note": note,
        }
    )


def add_source_inventory(packet: list[dict[str, str]]) -> None:
    sources = [
        (
            "U8_Kv100_open_dyno_temperature_loss_maps",
            MOTOR_THERMAL,
            U8_SOURCE,
            "open_bldc_temperature_loss_efficiency_map",
            "Larger U8/Kv100 motor/driver dyno maps; use for BLDC thermal direction and loss scale, not FPV 2306 coefficients.",
        ),
        (
            "MQTB_Emax_Eco_2306_FPV_motor_metadata",
            MOTOR_THERMAL,
            MQTB_SOURCE,
            "fpv_2306_hardware_class_metadata",
            "FPV-class motor KV, mass, stator, ESC rating, logger rate, and ambient metadata; no winding or ESC temperature rise.",
        ),
        (
            "Current_project_motor_ESC_thermal_model",
            MOTOR_THERMAL,
            DRONE_CONFIG_SOURCE,
            "current_thermal_limit_cooling_proxy",
            "Mirrors current thermal rise, cooling, motor/ESC limit, cutoff, and steady-rise formulas.",
        ),
        (
            "Current_project_ESC_electrical_stress_model",
            ESC_ELECTRICAL,
            DRONE_CONFIG_SOURCE,
            "current_power_current_headroom_proxy",
            "Mirrors current inferred KV, headroom, phase-current proxy, and shaft-power current proxy.",
        ),
        (
            "Copper_winding_temperature_coefficient",
            MOTOR_THERMAL,
            COPPER_SOURCE,
            "copper_resistance_temperature_shape",
            "Used for hot-winding resistance scaling; supports direction and slope, not motor cooling constants.",
        ),
        (
            "Motor_insulation_and_MOSFET_context",
            MOTOR_THERMAL,
            f"{MOTOR_INSULATION_SOURCE}; {INFINEON_MOSFET_SOURCE}",
            "thermal_limit_context",
            "Useful context for plausible motor/ESC temperature limits, but not direct FPV bench telemetry.",
        ),
    ]
    for name, source_file, source_url, role, note in sources:
        add_metric(
            packet,
            row_type="motor_thermal_packet_source_inventory",
            name=name,
            metric="source_file",
            value=repo_path(source_file),
            unit="path",
            source_file=source_file,
            source_url=source_url,
            evidence_role=role,
            note=note,
        )


def add_u8_dyno(packet: list[dict[str, str]], thermal_rows: list[dict[str, str]]) -> None:
    metrics = [
        ("voltage_v", "V"),
        ("temperature_sample_count", "count"),
        ("temperature_mean_c", "C"),
        ("temperature_max_c", "C"),
        ("loss_mean_w", "W"),
        ("loss_max_w", "W"),
        ("motor_efficiency_mean", "fraction"),
        ("motor_efficiency_max", "fraction"),
        ("driver_efficiency_mean", "fraction"),
        ("driver_efficiency_max", "fraction"),
        ("rpm_max", "rpm"),
        ("torque_max_nm", "N*m"),
        ("current_max_a", "A"),
        ("max_temp_current_a", "A"),
        ("max_temp_rpm", "rpm"),
        ("max_temp_torque_nm", "N*m"),
    ]
    for row in thermal_rows:
        if row.get("row_type") != "u8_dyno_processed":
            continue
        for metric, unit in metrics:
            add_metric(
                packet,
                row_type="motor_thermal_packet_u8_dyno_summary",
                name=row["name"],
                metric=metric,
                value=row.get(metric, ""),
                unit=unit,
                source_file=MOTOR_THERMAL,
                source_url=U8_SOURCE,
                evidence_role="larger_open_bldc_dyno_temperature_loss_anchor",
                note="Cross-class thermal/loss reference; not direct FPV 2306 motor calibration.",
            )


def add_mqtb_metadata(packet: list[dict[str, str]], thermal_rows: list[dict[str, str]]) -> None:
    row = require_one(thermal_rows, lambda item: item.get("row_type") == "reference_mqtb_fpv_motor_metadata")

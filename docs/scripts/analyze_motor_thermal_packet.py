        ("motor_limit_c", "C"),
        ("motor_cutoff_c", "C"),
        ("esc_limit_c", "C"),
        ("esc_cutoff_c", "C"),
        ("min_thermal_thrust_limit", "fraction"),
        ("motor_base_time_constant_s", "s"),

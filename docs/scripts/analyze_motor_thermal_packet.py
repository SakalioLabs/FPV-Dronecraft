def add_racing_crosscheck(
    packet: list[dict[str, str]],
    thermal_rows: list[dict[str, str]],
    electrical_rows: list[dict[str, str]],
) -> None:
    racing_thermal = require_one(
        thermal_rows,
        lambda row: row.get("row_type") == "current_preset_thermal" and row.get("name") == "racingQuad",
    )
    racing_mqtb = require_one(
        thermal_rows,
        lambda row: row.get("row_type") == "current_vs_mqtb_fpv_motor_metadata" and row.get("name") == "racingQuad",
    )
    racing_electrical = require_one(
        electrical_rows,
        lambda row: row.get("row_type") == "current_preset" and row.get("name") == "racingQuad",
    )
    u8_rows = [row for row in thermal_rows if row.get("row_type") == "u8_dyno_processed"]
    u8_max_temp = max(to_float(row, "temperature_max_c") for row in u8_rows)
    u8_max_loss = max(to_float(row, "loss_max_w") for row in u8_rows)
    u8_max_rise_vs_25c = u8_max_temp - REFERENCE_AMBIENT_C

    full_rise = to_float(racing_thermal, "motor_full_steady_rise_c")
    hover_rise = to_float(racing_thermal, "motor_hover_steady_rise_proxy_c")
    esc_full_rise = to_float(racing_thermal, "esc_full_current_steady_rise_proxy_c")
    motor_limit = to_float(racing_thermal, "motor_limit_c")
    motor_cutoff = to_float(racing_thermal, "motor_cutoff_c")
    esc_limit = to_float(racing_thermal, "esc_limit_c")
    esc_cutoff = to_float(racing_thermal, "esc_cutoff_c")

    metrics = [
        ("reference_ambient_c", REFERENCE_AMBIENT_C, "C", "assumed_ambient_for_derived_absolute_temperature"),
        ("u8_open_dyno_max_temperature_c", u8_max_temp, "C", "cross_class_u8_dyno_reference"),
        ("u8_open_dyno_max_loss_w", u8_max_loss, "W", "cross_class_u8_dyno_reference"),
        ("u8_max_temperature_rise_vs_25c", u8_max_rise_vs_25c, "C", "cross_class_u8_dyno_reference"),
        ("current_full_power_motor_absolute_temp_at_25c", REFERENCE_AMBIENT_C + full_rise, "C", "current_model_racingquad"),
        ("current_hover_motor_absolute_temp_at_25c", REFERENCE_AMBIENT_C + hover_rise, "C", "current_model_racingquad"),
        ("current_full_esc_absolute_temp_at_25c", REFERENCE_AMBIENT_C + esc_full_rise, "C", "current_model_racingquad"),
        ("current_full_motor_rise_over_motor_limit_margin", full_rise / max(motor_limit - REFERENCE_AMBIENT_C, 1e-9), "ratio", "current_model_racingquad"),
        ("current_full_motor_rise_over_motor_cutoff_margin", full_rise / max(motor_cutoff - REFERENCE_AMBIENT_C, 1e-9), "ratio", "current_model_racingquad"),
        ("current_full_esc_rise_over_esc_limit_margin", esc_full_rise / max(esc_limit - REFERENCE_AMBIENT_C, 1e-9), "ratio", "current_model_racingquad"),
        ("current_full_esc_rise_over_esc_cutoff_margin", esc_full_rise / max(esc_cutoff - REFERENCE_AMBIENT_C, 1e-9), "ratio", "current_model_racingquad"),
        ("current_full_motor_rise_over_u8_max_rise", full_rise / max(u8_max_rise_vs_25c, 1e-9), "ratio", "cross_class_u8_dyno_comparison"),
        ("current_hover_motor_rise_over_u8_max_rise", hover_rise / max(u8_max_rise_vs_25c, 1e-9), "ratio", "cross_class_u8_dyno_comparison"),
        ("current_inferred_kv_over_mqtb_tested_kv", to_float(racing_mqtb, "inferred_kv_over_mqtb_tested_kv"), "ratio", "fpv_2306_hardware_class_anchor"),
        ("current_per_motor_limit_over_mqtb_esc_rating", to_float(racing_mqtb, "per_motor_current_limit_over_mqtb_esc_rating"), "ratio", "fpv_2306_hardware_class_anchor"),
        ("current_max_power_current_over_limit", to_float(racing_electrical, "max_power_current_over_limit"), "ratio", "current_electrical_stress_input_to_thermal_model"),
        ("current_max_power_current_a", to_float(racing_electrical, "max_power_current_a"), "A", "current_electrical_stress_input_to_thermal_model"),
    ]
    for metric, value, unit, role in metrics:
        add_metric(
            packet,
            row_type="motor_thermal_packet_racingquad_crosscheck",
            name="racingQuad",
            metric=metric,
            value=value,
            unit=unit,
            source_file=MOTOR_THERMAL,
            source_url=f"{U8_SOURCE}; {MQTB_SOURCE}; {DRONE_CONFIG_SOURCE}",
            evidence_role=role,
            note="Cross-source handoff row; U8 is larger-motor dyno evidence, MQTB is FPV-class metadata, and current temperatures are model proxies.",
        )


def add_copper_scale(packet: list[dict[str, str]], thermal_rows: list[dict[str, str]]) -> None:
    for row in thermal_rows:
        if row.get("row_type") != "copper_resistance_scale":
            continue
        for metric, unit in [("temperature_c", "C"), ("resistance_scale_vs_25c", "ratio")]:
            add_metric(
                packet,
                row_type="motor_thermal_packet_copper_resistance_scale",
                name=row["name"],
                metric=metric,
                value=row.get(metric, ""),
                unit=unit,
                source_file=MOTOR_THERMAL,
                source_url=COPPER_SOURCE,
                evidence_role="hot_winding_resistance_shape",
                note="Copper coefficient supports hot-winding resistance scaling direction; cooling and heat capacity still need motor-specific data.",
            )


def add_method(packet: list[dict[str, str]]) -> None:
    add_metric(
        packet,
        row_type="motor_thermal_packet_method",
        name="method",
        metric="scope_and_caveat",
        value=(
            "Use this packet to separate hardware class, electrical stress, thermal limit/cutoff settings, and measured "
            "temperature/loss evidence. U8 rows are cross-class BLDC dyno anchors; MQTB rows are FPV-class metadata "
            "without temperature rise; current steady-rise rows remain model proxies until FPV thermocouple/IR telemetry is found."
        ),
        unit="text",
        source_file=MOTOR_THERMAL,
        source_url=f"{U8_SOURCE}; {MQTB_SOURCE}",
        evidence_role="method",
    )


def build_packet() -> list[dict[str, str]]:
    thermal_rows = read_rows(MOTOR_THERMAL)
    electrical_rows = read_rows(ESC_ELECTRICAL)

    packet: list[dict[str, str]] = []
    add_source_inventory(packet)
    add_u8_dyno(packet, thermal_rows)
    add_mqtb_metadata(packet, thermal_rows)
    add_current_thermal(packet, thermal_rows)
    add_electrical_stress(packet, electrical_rows)
    add_racing_crosscheck(packet, thermal_rows, electrical_rows)
    add_copper_scale(packet, thermal_rows)
    add_method(packet)
    return packet


def sync_summary(packet_rows: Iterable[dict[str, str]]) -> int:
    existing = read_rows(SUMMARY)
    kept = [row for row in existing if not row.get("category", "").startswith("motor_thermal_packet_")]
    added: list[dict[str, str]] = []
    for row in packet_rows:
        added.append(
            {
                "category": row["row_type"],
                "name": row["name"],
                "metric": row["metric"],
                "value": row["value"],
                "unit": row["unit"],
                "source": row.get("source_url") or row.get("source_file", ""),
            }
        )
    write_csv(SUMMARY, kept + added)
    return len(added)


def main() -> None:
    packet_rows = build_packet()
    write_csv(OUTPUT, packet_rows)
    synced = sync_summary(packet_rows)
    print(f"Wrote {repo_path(OUTPUT)} with {len(packet_rows)} rows")
    print(f"Synced {synced} rows into {repo_path(SUMMARY)}")


if __name__ == "__main__":
    main()

"""Build a prop pitch/geometry calibration packet.

Outputs:
  docs/data/prop_pitch_geometry_packet.csv
  docs/data/fpv_model_validation_summary.csv rows with category prop_pitch_packet_*

The source rows are generated by the main model-source analysis from official
HQProp/Gemfan pages, UIUC propeller geometry TXT files, and current RotorSpec
geometry mirrors. This packet narrows those rows into a handoff table for
per-preset blade-pitch, pitch-speed, chord proxy, and 70%R pitch-angle tuning.
"""

from __future__ import annotations

import csv
import math
from pathlib import Path
from typing import Iterable


ROOT = Path(__file__).resolve().parents[2]
DATA = ROOT / "docs" / "data"

OUTPUT = DATA / "prop_pitch_geometry_packet.csv"
SUMMARY = DATA / "fpv_model_validation_summary.csv"

PROP_GEOMETRY = DATA / "prop_geometry_pitch_reference.csv"

DRONE_CONFIG_SOURCE = "drone-sim-core/src/main/java/com/tenicana/dronecraft/sim/DroneConfig.java"
ROTOR_SPEC_SOURCE = "drone-sim-core/src/main/java/com/tenicana/dronecraft/sim/RotorSpec.java"
OFFICIAL_PROP_SOURCES = "HQProp/Gemfan official product pages listed in docs/data/prop_geometry_pitch_reference.csv"
UIUC_GEOMETRY_SOURCE = "https://m-selig.ae.illinois.edu/props/propDB.html"


def repo_path(path: Path) -> str:
    return path.resolve().relative_to(ROOT).as_posix()


def read_rows(path: Path) -> list[dict[str, str]]:
    with path.open(newline="", encoding="utf-8") as handle:
        return list(csv.DictReader(handle))


def write_csv(path: Path, rows: Iterable[dict[str, object]]) -> None:
    rows = list(rows)
    path.parent.mkdir(parents=True, exist_ok=True)
    fieldnames: list[str] = []
    for row in rows:
        for key in row:
            if key not in fieldnames:
                fieldnames.append(key)
    with path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(handle, fieldnames=fieldnames)
        writer.writeheader()
        for row in rows:
            writer.writerow(row)


def value_text(value: object) -> str:
    if value is None:
        return ""
    if isinstance(value, str):
        return value
    if isinstance(value, bool):
        return "1" if value else "0"
    if isinstance(value, int):
        return str(value)
    if isinstance(value, float):
        if not math.isfinite(value):
            return ""
        return f"{value:.12g}"
    return str(value)


def to_float(row: dict[str, str], key: str, default: float = math.nan) -> float:
    raw = row.get(key, "")
    if raw == "":
        return default
    try:
        return float(raw)
    except ValueError:
        return default


def clean(values: Iterable[float]) -> list[float]:
    return [value for value in values if math.isfinite(value)]


def add_metric(
    rows: list[dict[str, str]],
    *,
    row_type: str,
    name: str,
    metric: str,
    value: object,
    unit: str,
    source_file: Path,
    source_url: str = "",
    evidence_role: str = "",
    note: str = "",
) -> None:
    rows.append(
        {
            "row_type": row_type,
            "name": name,
            "metric": metric,
            "value": value_text(value),
            "unit": unit,
            "source_file": repo_path(source_file),
            "source_url": source_url,
            "evidence_role": evidence_role,
            "note": note,
        }
    )


def add_source_inventory(packet: list[dict[str, str]]) -> None:
    sources = [
        (
            "Current_RotorSpec_default_pitch_geometry",
            f"{DRONE_CONFIG_SOURCE}; {ROTOR_SPEC_SOURCE}",
            "current_default_pitch_and_geometry_proxy",
            "Current presets without overrides use RotorSpec.defaultBladePitchMeters(radius) = max(0.01, 1.70R), or P/D = 0.85 for current radii.",
        ),
        (
            "Official_HQProp_Gemfan_pitch_mass_pages",
            OFFICIAL_PROP_SOURCES,
            "official_product_diameter_pitch_mass",
            "Official product labels and mass rows for 5-inch FPV, 3-inch/ducted, and 10-inch lift prop families.",
        ),
        (
            "UIUC_propeller_geometry_TXT_files",
            UIUC_GEOMETRY_SOURCE,
            "station_geometry_r_over_R_c_over_R_beta",
            "UIUC geometry files provide r/R, c/R, and beta station data used to derive 70%R chord and local pitch ratios.",
        ),
        (
            "Generated_prop_geometry_reference",
            repo_path(PROP_GEOMETRY),
            "wide_source_table",
            "Wide table with current presets, official prop pitch rows, UIUC summaries, raw station rows, and current-vs-reference comparisons.",
        ),
    ]
    for name, source_url, role, note in sources:
        add_metric(
            packet,
            row_type="prop_pitch_packet_source_inventory",
            name=name,
            metric="source_file",
            value=repo_path(PROP_GEOMETRY),
            unit="path",
            source_file=PROP_GEOMETRY,
            source_url=source_url,
            evidence_role=role,
            note=note,
        )


def add_current_presets(packet: list[dict[str, str]], rows: list[dict[str, str]]) -> None:
    metrics = [
        ("current_diameter_in", "in"),
        ("current_pitch_in", "in"),
        ("current_pitch_to_diameter", "ratio"),
        ("current_hover_pitch_speed_m_s", "m/s"),
        ("current_max_pitch_speed_m_s", "m/s"),
        ("current_geometric_pitch_angle_70r_deg", "deg"),
        ("rotor_blade_count", "count"),
        ("blade_pitch_source", "text"),
    ]
    for row in rows:
        if row.get("row_type") != "current_preset_pitch_geometry":
            continue
        for metric, unit in metrics:
            add_metric(
                packet,
                row_type="prop_pitch_packet_current_preset",
                name=row["name"],
                metric=metric,
                value=row.get(metric, ""),
                unit=unit,
                source_file=PROP_GEOMETRY,
                source_url=f"{DRONE_CONFIG_SOURCE}; {ROTOR_SPEC_SOURCE}",
                evidence_role="current_geometry_proxy",
                note="Mirrors current RotorSpec pitch geometry; pitch speed is ideal geometric pitch with no slip.",
            )


def add_physical_refs(packet: list[dict[str, str]], rows: list[dict[str, str]]) -> None:
    metrics = [
        ("reference_diameter_in", "in"),
        ("reference_pitch_in", "in"),
        ("reference_pitch_to_diameter", "ratio"),
        ("reference_pitch_speed_m_s_at_1000rpm", "m/s per 1000 rpm"),
        ("reference_geometric_pitch_angle_70r_deg", "deg"),
        ("rotor_blade_count", "count"),
        ("mass_g", "g"),
    ]
    for row in rows:
        if row.get("row_type") != "physical_prop_pitch_reference":
            continue
        for metric, unit in metrics:
            add_metric(
                packet,
                row_type="prop_pitch_packet_official_prop_reference",
                name=row["name"],
                metric=metric,
                value=row.get(metric, ""),
                unit=unit,
                source_file=PROP_GEOMETRY,
                source_url=row.get("source", ""),
                evidence_role="official_product_pitch_mass_reference",
                note="Official product page reference; pitch label is geometric/nominal and not a slip-corrected aerodynamic pitch.",
            )



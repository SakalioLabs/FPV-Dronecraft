def add_crosscheck(packet: list[dict[str, str]], rows: list[dict[str, str]]) -> None:
    current = [row for row in rows if row.get("row_type") == "current_preset_pitch_geometry"]
    physical = [row for row in rows if row.get("row_type") == "current_vs_physical_prop_pitch_reference"]
    uiuc = [row for row in rows if row.get("row_type") == "current_vs_uiuc_prop_geometry_reference"]
    uiuc_points = [row for row in rows if row.get("row_type") == "reference_uiuc_prop_geometry_point"]
    uiuc_summaries = [row for row in rows if row.get("row_type") == "reference_uiuc_prop_geometry_summary"]

    def values(source_rows: list[dict[str, str]], preset: str, key: str) -> list[float]:
        return clean(to_float(row, key) for row in source_rows if row.get("preset") == preset)

    default_pd = clean(to_float(row, "current_pitch_to_diameter") for row in current)
    racing_phys = values(physical, "racingQuad", "current_pitch_over_reference")
    racing_uiuc_angle = values(uiuc, "racingQuad", "current_geometric_pitch_angle_over_reference_70r")
    racing_uiuc_chord = values(uiuc, "racingQuad", "current_proxy_chord_over_reference_70r")
    heavy_phys = values(physical, "heavyLift", "current_pitch_over_reference")
    heavy_phys_angle = values(physical, "heavyLift", "current_geometric_pitch_angle_over_reference")
    heavy_uiuc_angle = values(uiuc, "heavyLift", "current_geometric_pitch_angle_over_reference_70r")
    heavy_uiuc_local_pd = values(uiuc, "heavyLift", "current_pitch_to_diameter_over_reference_local_70r")
    heavy_uiuc_chord = values(uiuc, "heavyLift", "current_proxy_chord_over_reference_70r")

    metrics = [
        ("current_default_pitch_to_diameter_min", min(default_pd), "ratio"),
        ("current_default_pitch_to_diameter_max", max(default_pd), "ratio"),
        ("official_prop_reference_count", len([row for row in rows if row.get("row_type") == "physical_prop_pitch_reference"]), "count"),
        ("uiuc_geometry_summary_count", len(uiuc_summaries), "count"),
        ("uiuc_geometry_station_row_count", len(uiuc_points), "count"),
        ("current_vs_official_comparison_count", len(physical), "count"),
        ("current_vs_uiuc_comparison_count", len(uiuc), "count"),
        ("racing_official_pitch_ratio_min", min(racing_phys), "ratio"),
        ("racing_official_pitch_ratio_max", max(racing_phys), "ratio"),
        ("racing_uiuc_70r_pitch_angle_ratio_min", min(racing_uiuc_angle), "ratio"),
        ("racing_uiuc_70r_pitch_angle_ratio_max", max(racing_uiuc_angle), "ratio"),
        ("racing_uiuc_70r_chord_ratio_min", min(racing_uiuc_chord), "ratio"),
        ("racing_uiuc_70r_chord_ratio_max", max(racing_uiuc_chord), "ratio"),
        ("heavy_official_pitch_ratio_min", min(heavy_phys), "ratio"),
        ("heavy_official_pitch_ratio_max", max(heavy_phys), "ratio"),
        ("heavy_official_70r_angle_ratio_min", min(heavy_phys_angle), "ratio"),
        ("heavy_official_70r_angle_ratio_max", max(heavy_phys_angle), "ratio"),
        ("heavy_uiuc_70r_pitch_angle_ratio_min", min(heavy_uiuc_angle), "ratio"),
        ("heavy_uiuc_70r_pitch_angle_ratio_max", max(heavy_uiuc_angle), "ratio"),
        ("heavy_uiuc_70r_local_pd_ratio_min", min(heavy_uiuc_local_pd), "ratio"),
        ("heavy_uiuc_70r_local_pd_ratio_max", max(heavy_uiuc_local_pd), "ratio"),
        ("heavy_uiuc_70r_chord_ratio_min", min(heavy_uiuc_chord), "ratio"),
        ("heavy_uiuc_70r_chord_ratio_max", max(heavy_uiuc_chord), "ratio"),
    ]
    for metric, value, unit in metrics:
        add_metric(
            packet,
            row_type="prop_pitch_packet_crosscheck",
            name="summary",
            metric=metric,
            value=value,
            unit=unit,
            source_file=PROP_GEOMETRY,
            source_url=f"{OFFICIAL_PROP_SOURCES}; {UIUC_GEOMETRY_SOURCE}; {ROTOR_SPEC_SOURCE}",
            evidence_role="handoff_summary",
            note="Use these summary ratios to decide which presets need explicit blade-pitch/chord overrides.",
        )


def add_method(packet: list[dict[str, str]]) -> None:
    add_metric(
        packet,
        row_type="prop_pitch_packet_method",
        name="method",
        metric="scope_and_caveat",
        value=(
            "Use this packet for geometry handoff only: nominal prop pitch labels, UIUC station beta/chord, and current "
            "RotorSpec pitch/chord proxies. Do not treat label pitch as slip-corrected aerodynamic advance, and keep "
            "large-lift prop geometry separate from the 5-inch FPV default P/D = 0.85."
        ),
        unit="text",
        source_file=PROP_GEOMETRY,
        source_url=f"{OFFICIAL_PROP_SOURCES}; {UIUC_GEOMETRY_SOURCE}",
        evidence_role="method",
    )


def build_packet() -> list[dict[str, str]]:
    source_rows = read_rows(PROP_GEOMETRY)
    packet: list[dict[str, str]] = []
    add_source_inventory(packet)
    add_current_presets(packet, source_rows)
    add_physical_refs(packet, source_rows)
    add_uiuc_summaries(packet, source_rows)
    add_current_vs_physical(packet, source_rows)
    add_current_vs_uiuc(packet, source_rows)
    add_crosscheck(packet, source_rows)
    add_method(packet)
    return packet


def sync_summary(packet_rows: Iterable[dict[str, str]]) -> int:
    existing = read_rows(SUMMARY)
    kept = [row for row in existing if not row.get("category", "").startswith("prop_pitch_packet_")]
    added: list[dict[str, str]] = []
    for row in packet_rows:
        added.append(
            {
                "category": row["row_type"],
                "name": row["name"],
                "metric": row["metric"],
                "value": row["value"],
                "unit": row["unit"],
                "source": row.get("source_url") or row.get("source_file", ""),
            }
        )
    write_csv(SUMMARY, kept + added)
    return len(added)


def main() -> None:
    packet_rows = build_packet()
    write_csv(OUTPUT, packet_rows)
    synced = sync_summary(packet_rows)
    print(f"Wrote {repo_path(OUTPUT)} with {len(packet_rows)} rows")
    print(f"Synced {synced} rows into {repo_path(SUMMARY)}")


if __name__ == "__main__":
    main()
